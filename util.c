/*********************************************************************************
	FUNÇÃO		:	CALCULA_CRC
	PARAMETROS	:	INICIO FRAME -> PONTEIRO PARA O PRIMEIRO BYTE DO FRAME
				TAMANHO		_-> NUMERO DE BYTE QUE COMPOE O FRAME
	RETORNO		:	NENHUM
	DESCRIÇÃO	:	FAZ O CALCULO DO CRC DE 16 BITS PARA O A MENSAGEM.
*********************************************************************************/
void UTIL_calcula_crc(unsigned char *inicio_frame,unsigned char tamanho){
  unsigned char crc_low=0xff, crc_high=0xff,i,indice;

  if(tamanho<2)
    return;

  for(i=1 ;i<=tamanho; i++){
     indice = *inicio_frame ^ crc_high;
     crc_high = UTIL_crcHigh[indice] ^ crc_low;
     crc_low  = UTIL_crcLow[indice];
     inicio_frame++;
  }
  *inicio_frame = crc_high;
  inicio_frame++;
  *inicio_frame = crc_low;
}
/*********************************************************************************
	FUNÇÃO		:	CHECA_CRC
	PARAMETROS	:	INICIO_FRAME -> PONTEIRO PARA O PRIMEIRO BYTE DO FRAME
	                TAMANHO      -> NUMERO DE BYTES QUE COMPOE O FRAME
	RETORNO		:	NENHUM
	DESCRIÇÃO	:	FAZ O CALCULO PARA A CONFERENCIA DO CRC DE 16 BITS
*********************************************************************************/
unsigned char UTIL_checa_crc(unsigned char *inicio_frame, unsigned char tamanho){
  unsigned char crc_low=0xff, crc_high=0xff,i,indice;


  if(tamanho<2)
    return 0;

  for(i=1;i<=tamanho;i++){
     indice = *inicio_frame ^ crc_high;
     crc_high = UTIL_crcHigh[indice] ^ crc_low;
     crc_low  = UTIL_crcLow[indice];
     inicio_frame++;
   }
   if( *(inicio_frame+1) == crc_low && 
       *inicio_frame     == crc_high   )
    return(1);
   else
    return(0);
}
/*********************************************************************************
*       Descrição   :  Calcula o CRC e retorna um int16 com esse valor
*       Parametros  :  (unsigned char) *buffer
*                      (unsigned char) tamanho
*       Retorno     :  (unsigned int) CRC
*********************************************************************************/
unsigned int UTIL_calculaCRC16(unsigned char *buffer,unsigned char tamanho){
  unsigned char crc_low=0xff, crc_high=0xff,i,indice;

  for(i=1;i<=tamanho;i++){
     indice = *buffer ^ crc_high;
     crc_high = UTIL_crcHigh[indice] ^ crc_low;
     crc_low  = UTIL_crcLow[indice];
     buffer++;
   }
  
  return crc_high<<8 | crc_low;
}
