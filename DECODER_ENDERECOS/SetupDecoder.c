/***********************************************************************************
*   Descrição     :   Função para habilita/desabilitar os canais
*                     de decodificação de endereços
*   Parametros    :   (unsigned char) canal
*                     (unsigned char) flag, 1 para habilitado e vice-versa
*   Retorno       :   nenhum
***********************************************************************************/
void SETUPDECODER_enable(unsigned char channel,unsigned char flag){
 
  switch(channel){
    case 0: (flag)?(TPM2C0SC = 0b01001100):(TPM2C0SC = 0b00000000);
            break;   
    case 1: (flag)?(TPM2C1SC = 0b01001100):(TPM2C1SC = 0b00000000); 
            break;          
    case 2: (flag)?(TPM1C2SC = 0b01001100):(TPM1C2SC = 0b00000000); 
            break; 
    case 3: (flag)?(TPM1C3SC = 0b01001100):(TPM1C3SC = 0b00000000); 
            break;
  }
}
